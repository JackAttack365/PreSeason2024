package org.firstinspires.ftc.teamcode.util;

public class Coordinate {
    final double x;
    final double y;

    public Coordinate(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
